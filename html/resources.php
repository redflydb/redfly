  <a href="http://halfonlab.ccr.buffalo.edu/scrmshaw/SelectSmallestFeature.py"
     target="_blank">
     <strong>Utility script: SelectSmallestFeature.py</strong></a>
     Use this Python script with <?= HTML_REDFLY_LOGO ?> CRM sequences in sorted BED fortmat to cluster any overlapping sequences
     and select the set of shortest non-overlapping intervals from each cluster.
     <br>Helpful for constructing 
  <a href="http://halfonlab.ccr.buffalo.edu/scrmshaw.html"
     target="_blank">SCRMshaw</a> training sets and other uses.
